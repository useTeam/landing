// Función para obtener una traducción
export function getTranslation(key, language) {
  // Dividir la key por puntos para acceder a objetos anidados
  const keys = key.split('.')
  let value = translations

  // Navegar por el objeto de traducciones
  for (const k of keys) {
    if (!value[k]) {
      console.warn(`Translation key not found: ${key}`)
      return key
    }
    value = value[k]
  }

  // Devolver la traducción para el idioma especificado o la key si no existe
  return value[language] || key
}

// Hook para simplificar el uso de traducciones
export function useTranslations(language) {
  return (key) => getTranslation(key, language)
}
